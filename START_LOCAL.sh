echo "üöÄ –ó–∞–ø—É—Å–∫ —Å–∏—Å—Ç–µ–º—ã –∞–Ω–∞–ª–∏–∑–∞ –≥–æ–ª–æ—Å–∞ –Ω–∞ –±–æ–ª–µ–∑–Ω—å –ü–∞—Ä–∫–∏–Ω—Å–æ–Ω–∞ (–ª–æ–∫–∞–ª—å–Ω–æ)"
